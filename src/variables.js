var x = 12
console.log(x)
console.log(typeof x)

x = "hello"
console.log(x)
console.log(typeof x);



x.toUpperCase() // does not modify x - returns a new string
console.log(x);


var x = false // var allows re-declaration
console.log(x);
console.log(typeof x);


let y = 12;
console.log(y);

y = "hello"
console.log(y)

